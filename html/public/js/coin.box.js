$(document).ready(function () {
	var htmlContent = '<button class="btn btn-secondary dropdown-toggle" type="button" id="dropdownMenuButton" data-toggle="dropdown" aria-haspopup="true" aria-expanded="false">Singapore</button>';
	htmlContent += '<div class="dropdown-menu" aria-labelledby="dropdownMenuButton">';
	for (var i = 0; i < countryList.length; i++) {
		htmlContent += '<a class="dropdown-item" href="javascript:void(0)" onclick="fetchPeoples(\'' + countryList[i] + '\')">' + countryList[i] + '</a>';
	}
	htmlContent += '</div>';
	$("#countryDropDown").html(htmlContent);
	fetchPeoples(countryList[0]);
});


function fetchPeoples(countryName) {
	var filterPeople = userLists.filter(function (el) {
		return el.country === countryName;
	})
	if (filterPeople.length > 0) {
		var pplListHtmlContent = "";
		for (var i = 0; i < filterPeople.length; i++) {
			var startCont, endCont = "";
			if (i % 2 == 0) {
				startCont = '<div class="item"><div class="col-lg-12">';
				pplListHtmlContent += startCont;
			}
			pplListHtmlContent += '<div class="box_cat"><h3>' + filterPeople[i]['fName'] + '</h3>';
			pplListHtmlContent += '<p>' + filterPeople[i]['email'] + '</p></div>';
			if (i % 2 !== 0 || i == filterPeople.length - 1) {
				endCont = '</div></div>';
				pplListHtmlContent += endCont;
			}

		}
		$("#owl-carousel2").html(pplListHtmlContent);
		$("#dropdownMenuButton").html(countryName);
		triggerOwlCarouselChanged();
	}
}

function triggerOwlCarouselChanged() {
	if ($('#owl-carousel2').data('owl.carousel') !== undefined) {
		$('#owl-carousel2').data('owl.carousel').destroy();

	}

	var owlVariable = $('#owl-carousel2').owlCarousel({
		loop: true,
		margin: 0,
		nav: true,
		center: false,
		autoplay: true,
		dots: true,
		responsive: {
			0: {
				items: 1,
				center: false,
			},
			600: {
				items: 2,
				center: false,
			},
			769: {
				items: 4
			}
		},
	});
}